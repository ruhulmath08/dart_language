void main() {
  //Note: All the data type in dart are object
  //Therefore, there initial value is by default 'null'

  //Numbers: int
  int score = 23;
  var count = 23; //this is inferred as integer automatically by compiler

  //hex value
  int hexval = 0xe3e3e3e3;

  //Numbers: double
  double percentage = 93.5;
  var progress = 93.5;

  //Strings
  String name = "Ruhul";
  var man = "Ruhul";

  //Boolean
  bool isAlive = true;
  var alive = true;

  //print the value
  print(score);
  print(count);

  print(hexval);

  print(percentage);
  print(progress);

  print(name);
  print(man);

  print(isAlive);
  print(alive);
}
