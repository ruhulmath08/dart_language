void main() {
  //loop_control_statement
  /*
  * for
  * while
  * do-while
  * break and continue keyword
  * how to use labels in control flow statement
  * */

  //introduction to iterators

  //for loop
  for (var i = 0; i <= 5; i++) {
    print(i);
  }

  //while loop
  var i = 0;
  while (i <= 5) {
    print(i);
    i++;
  }

  //do-while loop
  var x = 1;
  do {
    print("Ruhul");
    x++;
  } while (x <= 5);

  /*
  * Ruhul
  * Ruhul
  * Ruhul
  * Ruhul
  * Ruhul
  */
}
